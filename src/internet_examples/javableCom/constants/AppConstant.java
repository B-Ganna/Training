package internet_examples.javableCom.constants;


public class AppConstant {
    static final byte C_CONSTANT = 4;

    public static void main(String[] args) {
        byte a = 7;
        String s = "Hello!";
        System.out.println(a);
        System.out.println(s);

        a = 3;
        s = "dadada";
        System.out.println(a);
        System.out.println(s);

        System.out.println(C_CONSTANT);

    }
}
